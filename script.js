function changeColor(element) {
    element.style.background = "linear-gradient(to bottom, #352c63, #473d7a)";
    element.style.boxShadow = "0 10px 20px rgba(0, 0, 0, 0.4)";
}

function restoreColor(element) {
    element.style.background = "linear-gradient(to bottom, #5a52a5, #473d7a)";
    element.style.boxShadow = "0 4px 8px rgba(0, 0, 0, 0.3)";
}